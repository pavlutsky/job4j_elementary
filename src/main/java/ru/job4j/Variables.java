package ru.job4j;

public class Variables {
    public static void main(String[] args) {
        System.out.print("age = ");
        byte age = 39;
        System.out.println(age);
        System.out.print("gender = ");
        char gender = 'M';
        System.out.println(gender);
        System.out.print("growth = ");
        float growth = 1.83F;
        System.out.println(growth);
        System.out.print("number of bricks = ");
        short bricks = 30720;
        System.out.println(bricks);
        System.out.print("number of bacteria = ");
        long bacteria = 350000000;
        System.out.println(bacteria);
        System.out.print("distance to the moon = ");
        double distance = 384467;
        System.out.println(distance);
        System.out.print("file size = ");
        int size = 18000000;
        System.out.println(size);
        System.out.print("permission to work = ");
        boolean permission = true;
        System.out.println(permission);

    }
}
